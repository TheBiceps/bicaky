// Recursively widen const literal types to string so translated dictionaries can satisfy the type
type DeepStringify<T> = T extends readonly (infer U)[]
  ? readonly DeepStringify<U>[]
  : T extends object
    ? { readonly [K in keyof T]: DeepStringify<T[K]> }
    : T extends string
      ? string
      : T extends number
        ? number
        : T;

